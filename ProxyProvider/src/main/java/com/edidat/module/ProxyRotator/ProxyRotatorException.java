package com.edidat.module.ProxyRotator;

public class ProxyRotatorException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 5239482294140790908L;

	public ProxyRotatorException() {
		super();
	}

	public ProxyRotatorException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ProxyRotatorException(String message, Throwable cause) {
		super(message, cause);
	}

	public ProxyRotatorException(String message) {
		super(message);
	}

	public ProxyRotatorException(Throwable cause) {
		super(cause);
	}

}
